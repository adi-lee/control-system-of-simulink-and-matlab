num=[1,2,2];
%由于k值的大小并不影响开环零极点的分布及开环零极点图的绘制，
%不妨令其为1，这样也能避免出现错误
den=conv([1,0],conv([1,4],conv([1,6],[1,4,4])));
G=tf(num,den)
figure(1);pzmap(G)  
[p,z]=pzmap(G)
figure(2);rlocus(G)
title('系统根轨迹图');%有重根所以只显示4个极点
figure(3);rlocus(G)
title('系统根轨迹图局部，用以确定临界根轨迹增益');
axis([-5 5 6 8]),[k2,p2]=rlocfind(G)
%求取临界根轨迹增益,经过选取几次范围确定范围，减小误差
%选取也需要看手准不准，可以多测几次

